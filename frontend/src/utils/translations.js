export const SUPPORTED_LANGUAGES = ['fr', 'en', 'ar'];
export const DEFAULT_LANGUAGE = 'fr';

export const LANGUAGE_OPTIONS = [
  { id: 'fr', label: 'Français', shortLabel: 'FR', dir: 'ltr', htmlLang: 'fr' },
  { id: 'en', label: 'English', shortLabel: 'EN', dir: 'ltr', htmlLang: 'en' },
  { id: 'ar', label: 'العربية', shortLabel: 'AR', dir: 'rtl', htmlLang: 'ar' },
];

export const LANGUAGE_DIRECTIONS = LANGUAGE_OPTIONS.reduce((acc, language) => {
  acc[language.id] = language.dir;
  return acc;
}, {});

export function normalizeLanguage(value, fallback = DEFAULT_LANGUAGE) {
  const normalized = String(value || '').trim().toLowerCase().slice(0, 2);
  return SUPPORTED_LANGUAGES.includes(normalized) ? normalized : fallback;
}

export function isRtlLanguage(language) {
  return LANGUAGE_DIRECTIONS[normalizeLanguage(language)] === 'rtl';
}

