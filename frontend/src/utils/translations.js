export function translate(language, key, replacements = {}) {
  const normalizedLanguage = normalizeLanguage(language);
  const text = translations[normalizedLanguage]?.[key]
    ?? translations[DEFAULT_LANGUAGE]?.[key]
    ?? translations.en?.[key]
    ?? key;

  return Object.entries(replacements).reduce(
    (value, [name, replacement]) => value.replaceAll(`{${name}}`, String(replacement ?? '')),
    text
  );
}

export function getLanguageOption(language) {
  return LANGUAGE_OPTIONS.find((item) => item.id === normalizeLanguage(language)) || LANGUAGE_OPTIONS[0];
}
